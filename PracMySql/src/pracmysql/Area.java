    //metodo que nos ayuda a mostar en nuestros cuadros de texto  
    //cuando vamos a modificar 
    
    void consulta(){
        
            String sql = "select * from areas where id ="+modif ;
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                rs = st.executeQuery(sql);
                Object[] persona = new Object[3];
                while (rs.next()) {
                    persona[0] = rs.getInt("id");
                    persona[1] = rs.getString("Nombre");
                    persona[2] = rs.getString("Ubicacion");
                }
                txtid.setText(persona[0].toString());
                txtNombre.setText(persona[1].toString());
                txtUbicacion.setText(persona[2].toString());
                
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e ,"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
            }
    }
