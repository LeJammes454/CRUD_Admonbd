    //metodo para agregar una nueva area
    void Agregar() {
        Areas area = new Areas(Integer.parseInt(txtid.getText()), txtNombre.getText(), txtUbicacion.getText());
        if (txtNombre.getText().equals("") || txtUbicacion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Las Cajas estan vacias!!!");
          
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(area.toString());
                JOptionPane.showMessageDialog(null, "Area agregada");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e ,"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    
    //metodo para modificar una area ya existente
    void modificar() {
        
      
        String nom = txtNombre.getText() ;
        String ubi = txtUbicacion.getText() ;
        
        String sql = "update areas set Nombre='" + nom + "',Ubicacion='" + ubi + "' where Id=" + modif;

        try {
            if (nom != null || ubi != null) {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Area Modificada");

            } else {
                JOptionPane.showMessageDialog(null, "Error...!!!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e,"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
        }
    }

    
