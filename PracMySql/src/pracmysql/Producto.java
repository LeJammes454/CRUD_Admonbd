    //metodo para relleñar nuestra jcombobox de areas con los datos que tenemos
    //almacenados en nuestra base de datos
    void combo() {
        String sql = "select Nombre from areas";
        try {
            cn = con.getConnection();
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet result = pst.executeQuery();   
            jComboArea.addItem("Selecione una opcion");
            while (result.next()) {
                jComboArea.addItem(result.getString("Nombre"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e , "ADVERTENCIA",JOptionPane.WARNING_MESSAGE);

        }
    }
  
    
    //Metodo apra agregar un nuevo producto a nuestra base de datos
    void agregrar() {
        //para sacar la fecha del jchoser 
        Date fecha = jDateChooser1.getDate();
        //para decalarar un formato que queremos que nos muestre la fecha en este cado sera dia/mes/año
        SimpleDateFormat formato = new SimpleDateFormat("d/MM/yyyy");

        
        Productos pro = new Productos(
                //recordemos que los text Field nos regresan un String
                //nosotros lo convertimos en un int 
                Integer.parseInt(txtId.getText()),
                txtNombre.getText(),
                txtDescripcion.getText(),
                txtSerie.getText(),
                txtColor.getText(),
                formato.format(fecha),
                jComboTipoAd.getSelectedItem().toString(),
                txtObservaciones.getText(),
                jComboArea.getSelectedIndex()
        );

        if (pro.getId()==-1) {
            JOptionPane.showMessageDialog(null, "Algunas Cajas estan vacias!!!", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(pro.toString());
                JOptionPane.showMessageDialog(null, "Producto agregado");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e , "ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    
    //metodo que usamos para poder modificar un producto del invetnario 
    //pensamos modificarlo para reducir lineas de codigo
    void modificar(){
        Date fecha = jDateChooser1.getDate();
        SimpleDateFormat formato = new SimpleDateFormat("d/MM/yyyy");
        
       
        String nom = txtNombre.getText();
        String des = txtDescripcion.getText();
        String ser = txtSerie.getText();
        String col = txtColor.getText();
        String fec = formato.format(fecha);
        String tip = jComboTipoAd.getSelectedItem().toString();
        String obs = txtObservaciones.getText();
        //llamamos el metodo extramodificar para saber a que area pertenece en nuestra tabla de areas
        extramodificar();
        int area = are;
        
        
        //realizamos la consulta sql a nuestra base de datos
        String sql = "update inventario set "
                + "NombreCorto='" + nom +
                "',Descripcion='" +des +
                "',Serie='" +ser + 
                "',Color='" +col + 
                "',FechaAdquision='"+ fec +
                "',TipoAdquisicion='" + tip +
                "',Observaciones='" + obs +
                "',AREAS_id=" + area + 
                " where Id=" + aux;

        try {
            if (nom != null || des != null) {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Producto Modificada");
                

            } else {
                JOptionPane.showMessageDialog(null,"ERROR!!!", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e,"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
        }
    }
    
    
