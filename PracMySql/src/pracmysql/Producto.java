    //metodo para realizar una previa consulta cuadno seleccionamos el boton modificar 
    //lo utilizamos para rellenar los datos en nuestras cajas del producto que previamente seleccionamos 
    void consulta(){
            
            //nuestra consulta sql
            String sql = "select * from inventario where id ="+ aux ;
            try {
                
                //nuestras connexiones
                cn = con.getConnection();
                st = cn.createStatement();
                rs = st.executeQuery(sql);
                
                
                Object[] persona = new Object[9];
                while (rs.next()) {
                persona[0] = rs.getInt("id");
                persona[1] = rs.getString("NombreCorto");
                persona[2] = rs.getString("Descripcion");
                persona[3] = rs.getString("Serie");
                persona[4] = rs.getString("Color");
                persona[5] = rs.getString("FechaAdquision");
                persona[6] = rs.getString("TipoAdquisicion");
                persona[7] = rs.getString("Observaciones");
                persona[8] = rs.getString("AREAS_id");
                }
                txtId.setText(persona[0].toString());
                txtNombre.setText(persona[1].toString());
                txtDescripcion.setText(persona[2].toString());
                txtSerie.setText(persona[3].toString());
                txtColor.setText(persona[4].toString());
                txtObservaciones.setText(persona[7].toString());
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }
    
    
    // metodo que utlizamos para saber exacatamente que la id del area  
    void extramodificar(){
        
        String sql = "select id from areas where Nombre = '"+jComboArea.getSelectedItem().toString()+"'";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
               are = rs.getInt("id");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e ,"ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
        }
    }
    
   
    
